// ─── Helpers ──────────────────────────────────────────────────────────────

export function getProductBySlug(slug: string): Product | undefined {
  return PRODUCTS.find((p) => p.slug === slug)
}

export function getProductsByLine(line: Product['line']): Product[] {
  return PRODUCTS.filter((p) => p.line === line)
}

export function getProductsByBrand(brand: Product['brand']): Product[] {
  return PRODUCTS.filter((p) => p.brand === brand)
}

export function getFeaturedProducts(): Product[] {
  return PRODUCTS.filter((p) => p.featured)
}

export function getRelatedProducts(product: Product, limit = 4): Product[] {
  return PRODUCTS
    .filter((p) => p.id !== product.id && (p.line === product.line || p.crops.some((c) => product.crops.includes(c))))
    .slice(0, limit)
}

export function searchProducts(query: string): Product[] {
  const q = query.toLowerCase()
  return PRODUCTS.filter(
    (p) =>
      p.name.toLowerCase().includes(q) ||
      p.description.toLowerCase().includes(q) ||
      p.solves_problems.some((s) => s.toLowerCase().includes(q)) ||
      p.category.toLowerCase().includes(q),
  )
}
